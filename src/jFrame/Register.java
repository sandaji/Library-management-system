   public boolean insertRegisterDetails(){
        String name = input_username.getText();
        String pwd = input_password.getText();
        String mail = input_email.getText();
        String phone = input_contact.getText();
        String staffid = input_staff_id.getText();
        
        
  
        try {
            Connection con = DBConnection.getConnection();
            String sql = "insert into users(name,password,email,contact,staff_id) values(?,?,?,?,?)";
            PreparedStatement pst= con.prepareStatement(sql);

            pst.setString(1, name);
            pst.setString(2, pwd);
            pst.setString(3, mail);
            pst.setString(4, phone);
            pst.setString(5, staffid);
       

            int updatedRowCount = pst.executeUpdate();

            if (updatedRowCount> 0 ){
                JOptionPane.showMessageDialog(this, "registration succefull");
                Login login = new Login();
                login.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "registration failed");
            }
        } catch(HeadlessException | SQLException e){
        }
        return false;

  
    }
