    public boolean validateRegister(){
        String name = input_username.getText();
        String pwd = input_password.getText();
        String mail = input_email.getText();
        String phone = input_contact.getText();
        String staffid = input_staff_id.getText();
        
        if (name.equals("")){
           JOptionPane.showInputDialog(this, "please enter your name");
           return false;
        }        
        if (pwd.equals("")){
           JOptionPane.showInputDialog(this, "please enter your password");
           return false;
        }        
        if (mail.equals("") || !mail.matches("^.+@.+\\..+$")){
           JOptionPane.showInputDialog(this, "please enter a valid email");
           return false;
        }        
        if (phone.equals("")){
           JOptionPane.showInputDialog(this, "please enter your phone number");
           return false;
        }        
        if (staffid.equals("")){
           JOptionPane.showInputDialog(this, "please enter your staff ID number");
           return false;
        }
        
        return true;
        }
